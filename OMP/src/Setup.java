public class Setup {
    private int from;
    private int to;
    private int start;

    public Setup(int from, int to, int start) {
        this.from = from;
        this.to = to;
        this.start = start;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }
}
